# Write a program to display day of the week., Mon, Tue, Wed, Thu, Fri, Sat, Sun.

n=int(input("enter the number:"))
if n==1:
    print("mon")
if n==2:
    print("tue")
if n==3:
    print("wed")
if n==4:
    print("thu")
if n==5:
    print("fri")
if n==6:
    print("sat")
if n==7:
    print("sun")       
else:
    print("exit")                    